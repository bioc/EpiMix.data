# The data are lncRNA expression data for tumors from the TCGA cancer dataset

# Raw sequencing reads (fastq format) from RNA-Seq were downloaded from ISB-CGC (https://isb-cgc.appspot.com/) and
# were preprocessed and quantified using the procedures described in our previous study (Zheng et al, Gigascience, PMID: 31808800, 2019).

# The data were also stored in the github repo: https://github.com/gevaertlab/lncRNA_pancancer


